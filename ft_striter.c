#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t n;

	if (s != NULL && (*f) != NULL)
	{
		n = 0;
		while (s[n])
		{
			(*f)(&s[n]);
			n++;
		}
	}
}
