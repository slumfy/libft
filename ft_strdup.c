#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dst;
	int		i;

	if (!(dst = (char*)malloc(sizeof(char) * ft_strlen(s1) + 1)))
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = s1[i];
	return (dst);
}
