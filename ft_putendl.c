#include "libft.h"

void	ft_putendl(char const *s)
{
	size_t i;

	i = 0;
	if (s == 0)
		return ;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
